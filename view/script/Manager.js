

function clickDelete() {
	document.getElementById("managerNameField").style.display="block";
	document.getElementById("managerPasswordField").style.display="none";
	document.getElementById("managerTypeField").style.display="none";
	document.getElementById("managerSubmitField").style.display="block";
} 
function clickAdd() {
	document.getElementById("managerNameField").style.display="block";
	document.getElementById("managerPasswordField").style.display="block";
	document.getElementById("managerTypeField").style.display="block";
	document.getElementById("managerSubmitField").style.display="block";
}
function clickUpdate() {
	document.getElementById("managerNameField").style.display="block";
	document.getElementById("managerPasswordField").style.display="none";
	document.getElementById("managerTypeField").style.display="block";
	document.getElementById("managerSubmitField").style.display="block";
}